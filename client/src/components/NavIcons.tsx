// UI/UX overhaul 2026-07-07 (W2): single home for the nav glyphs.
// BottomNav and SideRail used to each carry byte-identical copies of
// these SVGs with "keep the two in sync" comments — the classic
// drift trap. Both now import from here, so a glyph change lands in
// every nav surface at once.
//
// Every icon carries `aria-hidden="true"` (iter-356.56 Dana #2) so
// screen-reader swipe never lands on a "graphic" node before the
// visible label; the NavLink/aria-label owns the accessible name.
//
// `active` is optional — only LiveIcon renders differently when
// active (the red recording dot). The prop stays on the shared
// signature so callers can pass it uniformly.

